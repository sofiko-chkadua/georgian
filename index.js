const submit = document.getElementById("submit");
const button = document.querySelector("#submit");

let currentQuiz = 0;
let score = 0;

loadQuiz();

function loadQuiz() {
  deselectAnswers();

  const currentQuizData = quizData[currentQuiz];

  questionElement.innerText = currentQuizData.question;
  a_text.innerText = currentQuizData.ა;
  b_text.innerText = currentQuizData.ბ;
  c_text.innerText = currentQuizData.გ;
  d_text.innerText = currentQuizData.დ;
}

function deselectAnswers() {
  answerElements.forEach((answerEl) => (answerEl.checked = false));
}

function getSelected() {
  let answer;

  answerElements.forEach((answerEl) => {
    if (answerEl.checked) {
      answer = answerEl.id;
    }
  });

  return answer;
}
submit.addEventListener("click", () => {
  const answer = getSelected();

  console.log(answer, quizData[currentQuiz].correc);

  if (answer) {
    if (answer === quizData[currentQuiz].correct) {
      score++;

      button.style.backgroundColor = "#5ec6eb";
    } else {
      button.style.backgroundColor = "salmon";
      return;
    }

    currentQuiz++;

    if (currentQuiz < quizData.length) {
      loadQuiz();
    } else {
      quiz.innerHTML = `<h2>You answered coreectly at ${score}/${quizData.length} questions</h2>
            <button onclick="location.reload()">Reload</button>
            `;
    }
  }
});
